export const calculus =(county, quater, numberOfBed)=>{
    let result = []
    if (!county || !numberOfBed){
        return
    }
    let daily = CountyData[county][quater][numberOfBed][0];
    let avg = CountyData[county][quater][numberOfBed][1];

    if (daily === 0 || avg ===0){
        result = [0,0]
        return result
    }
    
    let quaterly =  daily * avg * 90
    let monthly = quaterly / 3

    quaterly = Math.round(quaterly).toLocaleString("en-us");
    monthly = Math.round(monthly).toLocaleString("en-us");
    
    result = [quaterly, monthly];

    return result
    
}